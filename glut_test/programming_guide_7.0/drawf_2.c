int i=0, j=0;
GLubyte* modeByte;
void ChangeWord16(GLubyte *modeByte)
{
	for (i=0; i<16; )
	{
		GLubyte temp1 = modeByte[i];
		GLubyte temp2 = modeByte[i+1];
		modeByte[i] = modeByte[32-i-2];
		modeByte[i+1] = modeByte[32-i-1];
		modeByte[32-i-2] = temp1;
		modeByte[32-i-1] = temp2;
		i = i+2;
	}
}

void ChangeWord64(GLubyte *modeByte)
{
	for (i = 0; i < 64;)
	{
		GLubyte temp[8];
		for (j = 0; j < 8; j++)
		{
			temp[j] = modeByte[511-i-9+j];
		}
		for (j = 0; j < 8; j++)
		{
			modeByte[i+j] = modeByte[512-i-1-j];
		}
		for (j = 0; j < 8; j++)
		{
			modeByte[512-i-1-j] = temp[8-j-1];
		}
		i = i+8;
	}
}

